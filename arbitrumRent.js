  function getIdProof(tokenId) {
    const leafNodesT1 = tierOne.map(id => keccak256(id));
    const leafNodesT2 = tierTwo.map(id => keccak256(id));
    const leafNodesT3 = tierThree.map(id => keccak256(id));
    const merkleTreeT1 = new MerkleTree(leafNodesT1, keccak256, { sortPairs: true});
    const merkleTreeT2 = new MerkleTree(leafNodesT2, keccak256, { sortPairs: true});
    const merkleTreeT3 = new MerkleTree(leafNodesT3, keccak256, { sortPairs: true});
    const tokenHash = keccak256(tokenId);
    const proofT1 = merkleTreeT1.getHexProof(tokenHash);
    const proofT2 = merkleTreeT2.getHexProof(tokenHash);
    const proofT3 = merkleTreeT3.getHexProof(tokenHash);

    if (proofT1.length > 0) {
        return proofT1;
    } else if (proofT2.length > 0) {
        return proofT2;
    } else if (proofT3.length > 0) {
        return proofT3;
    } else {
        return [];
    }
  }

  function toHexString(byteArray) {
    return Array.prototype.map.call(byteArray, function(byte) {
      return ('0' + (byte & 0xFF).toString(16)).slice(-2);
    }).join('');
  }

  function parseHex(b) {
    return b.match(/.{4}/g).reduce(function(acc, i) {
        return acc + parseInt(i, 2).toString(16);
    }, '')
  }

  function parseText(binary) {
    binary = binary.split(' ');
    return binary.map(elem => String.fromCharCode(parseInt(elem, 2))).join("");
  }

